package class7;

public class E12WhileLoop {
    public static void main(String[] args) {

        // add all the number from 1 to 10 and display the result 55

        int num = 1;
        int sum = 0;
        while (num <= 10) {
            sum += num;
            num++;


        }
        System.out.println(sum);

    }
}