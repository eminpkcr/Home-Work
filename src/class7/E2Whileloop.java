package class7;

public class E2Whileloop {
    public static void main(String[] args) {

        int number=0;
        //below we are using the increment operator
        number++;   // 0-> 1
        number++;   // 1-> 2
        System.out.println(number);

        int num=0;
        num--;
        num--;
        System.out.println(num);


    }
}
